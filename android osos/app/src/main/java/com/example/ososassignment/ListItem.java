package com.example.ososassignment;

public class ListItem {
    private String name;
    private String age;
    private String roll;

    public ListItem(String name, String age, String roll) {
        this.name = name;
        this.age = age;
        this.roll = roll;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public String getRoll() {
        return roll;
    }


}
